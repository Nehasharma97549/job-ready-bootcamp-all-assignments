//print size of data type//
#include<stdio.h>
int main()
{
    printf("sizeof int is: %d\n", sizeof(int));//4 bytes//
    printf("sizeof char is: %d\n", sizeof(char));//4 bytes//
    printf("sizeof float is: %d\n", sizeof(float));//1 bytes//
    printf("sizeof double is: %d\n",sizeof(double));//8 bytes//

    return 0;
}
